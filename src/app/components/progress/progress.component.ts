import { Component, Input } from '@angular/core';
import { NgIf } from '@angular/common';

@Component({
  selector: 'components-progress',
  templateUrl: './progress.component.html',
  styleUrls: ['./progress.component.css'],
  standalone: true,
  imports: [
    NgIf
  ]
})
export class ProgressComponent {

  /* *************************************** INPUTS & OUTPUTS ***************************************** */
  @Input('totalHours') totalHours: number = 0;
  @Input('busyHours') busyHours: number = 0;
  /* *************************************** ---------------- ***************************************** */


  /* ************************************ LISTAS GETTERS SETTERS ************************************** */

  /* *********************************** ------------------ ******************************************* */


  /* *********************************** VARIABLES GLOBALES ******************************************* */

  progress: number = 0;
  /* *********************************** ------------------ ******************************************* */


  /* *********************************** COSTRUCTOR Y CICLO DE VIDA *********************************** */

  constructor(){}

  /* *********************************** -------------------------- *********************************** */


  /* *********************************** GETERS Y SETERS ********************************************** */

  get getPercentage(){
    if(this.totalHours === 0){
      this.progress = 0;
    }else{
      this.progress = Math.ceil((this.busyHours * 100 ) / this.totalHours);
    }
    return `${ this.progress }%`
  }
  /* *********************************** -------------------------- *********************************** */


  /* *********************************** FORMULARIOS DEL FORMULARIO *********************************** */

  /* *********************************** -------------------------- *********************************** */


  /* *********************************** FUNCIONES VARIAS ********************************************* */

  /* *********************************** -------------------------- *********************************** */
}
